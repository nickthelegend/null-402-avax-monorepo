/**
 * Convert snarkjs artifacts → null-402 verifier contract byte inputs.
 *
 * Reads build/{verification_key,proof,public}.json and writes
 * build/contract-inputs.json with hex-encoded BytesN for the contract's
 * `init(vk)` and `verify(proof, public_inputs)` calls.
 *
 * Encoding: uncompressed affine, 32-byte big-endian field elements.
 *   G1 (64B)  = x ‖ y
 *   G2 (128B) = x.c1 ‖ x.c0 ‖ y.c1 ‖ y.c0   (EIP-197 / BN254 "imaginary first")
 *
 * NOTE: the G2 component order (c1‖c0 vs c0‖c1) must be confirmed with one
 * on-chain round-trip against a known-valid proof; flip `G2_IMAG_FIRST` if the
 * pairing check returns false for a proof snarkjs accepts.
 */

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";

const G2_IMAG_FIRST = true; // EIP-197 convention; flip if on-chain verify fails

const dir = "build";
const vk = JSON.parse(readFileSync(`${dir}/verification_key.json`, "utf8"));
const proof = JSON.parse(readFileSync(`${dir}/proof.json`, "utf8"));
const publicSignals = JSON.parse(readFileSync(`${dir}/public.json`, "utf8"));

function be32(dec) {
  let n = BigInt(dec);
  const out = new Uint8Array(32);
  for (let i = 31; i >= 0; i--) {
    out[i] = Number(n & 0xffn);
    n >>= 8n;
  }
  return Buffer.from(out).toString("hex");
}

const g1 = (p) => be32(p[0]) + be32(p[1]); // 64 bytes
const g2 = (p) =>
  G2_IMAG_FIRST
    ? be32(p[0][1]) + be32(p[0][0]) + be32(p[1][1]) + be32(p[1][0])
    : be32(p[0][0]) + be32(p[0][1]) + be32(p[1][0]) + be32(p[1][1]); // 128 bytes

const out = {
  vk: {
    alpha: g1(vk.vk_alpha_1),
    beta: g2(vk.vk_beta_2),
    gamma: g2(vk.vk_gamma_2),
    delta: g2(vk.vk_delta_2),
    ic: vk.IC.map(g1),
  },
  proof: {
    a: g1(proof.pi_a),
    b: g2(proof.pi_b),
    c: g1(proof.pi_c),
  },
  public_inputs: publicSignals.map(be32),
};

writeFileSync(`${dir}/contract-inputs.json`, JSON.stringify(out, null, 2));
console.log("wrote build/contract-inputs.json");
console.log(`  ic points: ${out.vk.ic.length} (expect nPublic+1 = ${out.public_inputs.length + 1})`);
console.log(`  a=${out.proof.a.length / 2}B  b=${out.proof.b.length / 2}B  c=${out.proof.c.length / 2}B`);

// ── Also emit a Rust fixture so the verifier contract's host test can verify
//    this exact proof (confirms byte encoding + pairing logic). ───────────────
const bytes = (h) => h.match(/../g).map((b) => parseInt(b, 16));
const arr = (h) => `[${bytes(h).join(",")}]`;
const arrN = (a) => `[\n    ${a.map(arr).join(",\n    ")},\n]`;

const fixture = `// @generated by null-402-circuits/scripts/export-contract-inputs.mjs — do not edit.
// A real, snarkjs-verified proof + its verifying key, for the host pairing test.
pub const ALPHA: [u8; 64] = ${arr(out.vk.alpha)};
pub const BETA: [u8; 128] = ${arr(out.vk.beta)};
pub const GAMMA: [u8; 128] = ${arr(out.vk.gamma)};
pub const DELTA: [u8; 128] = ${arr(out.vk.delta)};
pub const IC: [[u8; 64]; ${out.vk.ic.length}] = ${arrN(out.vk.ic)};
pub const PROOF_A: [u8; 64] = ${arr(out.proof.a)};
pub const PROOF_B: [u8; 128] = ${arr(out.proof.b)};
pub const PROOF_C: [u8; 64] = ${arr(out.proof.c)};
pub const PUBLIC: [[u8; 32]; ${out.public_inputs.length}] = ${arrN(out.public_inputs)};
`;

const fixtureDir = "../null-402-contracts/verifier/tests";
mkdirSync(fixtureDir, { recursive: true });
writeFileSync(`${fixtureDir}/fixture.rs`, fixture);
console.log(`wrote ${fixtureDir}/fixture.rs`);
